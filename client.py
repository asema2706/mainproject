import socket      # библиотека для работы с сетью
import threading   # библиотека для работы с потоками

# ── Глобальная переменная ─────────────────────────────────────────────────────

# Флаг: True — клиент работает, False — нужно завершиться
# Используется, чтобы корректно остановить оба потока
running = True


# ── Поток 1: получение сообщений от сервера ───────────────────────────────────

def receive_messages(client_socket):
    """
    Эта функция работает в отдельном потоке.
    Она постоянно слушает сервер и выводит новые сообщения на экран.
    """
    global running  # используем глобальную переменную

    while running:
        try:
            # recv(1024) — ждём данные от сервера (блокирующий вызов)
            # Поток «спит» здесь, пока сервер что-нибудь не пришлёт
            data = client_socket.recv(1024)

            # Если recv вернул пустые данные — сервер закрыл соединение
            if not data:
                print("\n[!] Соединение с сервером разорвано.")
                running = False  # сигнализируем второму потоку о завершении
                break

            # Декодируем байты в текст (уровень 6 OSI — представительский)
            message = data.decode("utf-8")
            print(f"\n{message}")

        except Exception as e:
            # Ошибка соединения (например, сервер упал)
            if running:  # выводим ошибку только если не сами завершились
                print(f"\n[!] Ошибка при получении сообщения: {e}")
            running = False
            break

    # Поток завершил работу
    print("[*] Поток получения сообщений завершён.")


# ── Поток 2 (главный): отправка сообщений ────────────────────────────────────

def send_messages(client_socket):
    """
    Эта функция работает в главном потоке.
    Она читает ввод пользователя и отправляет сообщения на сервер.
    """
    global running  # используем глобальную переменную

    while running:
        try:
            # input() — блокирующий вызов: ждём, пока пользователь введёт текст
            message = input()

            # Если флаг сброшен — выходим (сервер уже отключился)
            if not running:
                break

            # Кодируем текст в байты (уровень 6 OSI — представительский)
            # и отправляем на сервер
            client_socket.send(message.encode("utf-8"))

        except Exception as e:
            print(f"[!] Ошибка при отправке сообщения: {e}")
            running = False
            break


# ── Главная функция — подключение к серверу ───────────────────────────────────

def main():
    HOST = "127.0.0.1"   # адрес сервера (localhost — тот же компьютер)
    PORT = 5001           # порт сервера (должен совпадать с server.py)

    # Создаём сокет: AF_INET = IPv4 (уровень 3 OSI), SOCK_STREAM = TCP (уровень 4)
    client_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

    # Подключаемся к серверу (блокирующий вызов — ждём, пока сервер ответит)
    try:
        client_socket.connect((HOST, PORT))
        print(f"[*] Подключились к серверу {HOST}:{PORT}")
        print("[*] Пишите сообщения и нажимайте Enter для отправки.")
        print("[*] Для выхода нажмите Ctrl+C\n")
    except Exception as e:
        print(f"[!] Не удалось подключиться к серверу: {e}")
        return  # выходим из программы

    # ── Запускаем поток для получения сообщений ───────────────────────────────
    # Поток будет работать параллельно с главным потоком
    receive_thread = threading.Thread(
        target=receive_messages,   # функция для потока
        args=(client_socket,)      # аргументы функции
    )
    receive_thread.daemon = True   # поток завершится вместе с программой
    receive_thread.start()         # запускаем поток

    # ── Главный поток занимается отправкой сообщений ──────────────────────────
    # Это не отдельный поток — это просто продолжение основной программы
    try:
        send_messages(client_socket)
    except KeyboardInterrupt:
        # Пользователь нажал Ctrl+C — завершаем программу
        print("\n[*] Выход из чата...")

    # ── Завершение работы ─────────────────────────────────────────────────────
    global running
    running = False          # останавливаем оба потока
    client_socket.close()   # закрываем соединение с сервером
    print("[*] Соединение закрыто.")


# Запускаем клиент при прямом запуске файла
if __name__ == "__main__":
    main()
